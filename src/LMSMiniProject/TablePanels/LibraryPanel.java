    private void InsertButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertButton1ActionPerformed
        
        String query1 = "SELECT * FROM library WHERE library_id = '" + library_id + "'";
        String query2 = "SELECT b.book_id, bd.title FROM book b, book_details bd WHERE b.isbn = bd.isbn AND b.availible = 1 AND b.library_id = '" + library_id + "' ORDER BY book_id";
        String query3 = "SELECT p.patron_id, c.cust_fn, c.cust_ln FROM patron p, customer c WHERE p.cust_id = c.cust_id AND library_id = '" + library_id + "'";

        try {
            st = con.createStatement();
            rs = st.executeQuery(query1);

            rs.next();

            libName.setText(rs.getString(2));
            locId.setText(rs.getString(3));
            year.setText(rs.getString(4));
            rating.setValue(Integer.parseInt(rs.getString(4)));

            st = con.createStatement();
            rs = st.executeQuery(query2);

            while(rs.next()) {
                lib_det.add(new Entry(rs.getString(1), rs.getString(2)));
            }
            lib_det.repaint();
            lib_det.revalidate();

            st = con.createStatement();
            rs = st.executeQuery(query3);

            while(rs.next()) {
                pat_det.add(new Entry(rs.getString(1), rs.getString(2)));
            }
            pat_det.repaint();
            pat_det.revalidate();

        }
        catch (SQLException ex) {

            Logger.getLogger(LibraryPanel.class.getName()).log(Level.SEVERE, null, ex);

            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(this, "Invalid input present in form.");
        }
    }//GEN-LAST:event_InsertButton1ActionPerformed

