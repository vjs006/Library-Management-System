    private void InsertButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertButton3ActionPerformed
        for (int i = 0; i < Integer.parseInt(quantity.getText()); i++) {
        String query = "SELECT book_id FROM book WHERE library_id = '" + library_id + "'";
        int newId = 0;
        int prevId = 0;
        int finalId = 0;
        boolean chk = false;
        
        try {
            st = con.createStatement();
            rs = st.executeQuery(query);
            
            if(rs.next())
                prevId = Integer.parseInt(rs.getString(1).substring(1));
            
            while ((chk = rs.next())) {
                newId = Integer.parseInt(rs.getString(1).substring(1));
                System.out.println(prevId + " " + newId);
                if (newId - prevId > 1) {
                    finalId = prevId + 1;
                    break;
                }
                prevId = newId;
            }
            
            if (chk == false)
                finalId = newId + 1;
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
        
        System.out.println(finalId);
        String bookid = Integer.toString(finalId);
        
        if (bookid.length() == 1) {
            bookid = "B00" + bookid;
        }
        else {
            bookid = "B0" + bookid;
        }
        
        System.out.println(bookid);
        String query2 = "INSERT INTO book VALUES(?, ?, ?, ?, ?)";
        try {
            ps = con.prepareStatement(query2);
            ps.setString(1, bookid);
            ps.setString(2, library_id);
            ps.setString(3, isbn.getText());
            ps.setFloat(4, Float.parseFloat(price.getText()));
            ps.setInt(5, 1);
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Inserted book");
        }
        catch (SQLException ex) {
            Logger.getLogger(LibraryPanel.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid input present in form.");
        }
        }
    }//GEN-LAST:event_InsertButton3ActionPerformed

